// =========== ARITHMETICAL OPERATORS =========== 
let a = 24, b = 19;

// ========== ADD -> + SIGN ========== 
let c = a + b;
console.log("Addition: " + c);

// ========== SUBS -> - SIGN ========== 
c = a - b;
console.log("Substraction: " + c);

// ========== MUL -> * SIGN ========== 
c = a * b;
console.log("Multiplication: " + c);

// ========== DIV -> / SIGN ========== 
c = a / b;
console.log("Division: " + c);

// ========== MOD -> % SIGN ========== 
c = a % b;
console.log("Modulus: " + c);

// ========== POW -> ** SIGN ========== 
c = a ** c;
console.log("Power: " + c)